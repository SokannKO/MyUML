package net.skanny.myuml;

public class ClassDining {
	private String udc, icc, ccf, hne;



	public ClassDining(){}


	public String getUDC() {return udc;	}
	public void setUDC(String udc) {this.udc = udc;	}
	public String getICC() {return icc;	}
	public void setICC(String icc) {this.icc = icc;	}
	public String getCCF() {return ccf;	}
	public void setCCF(String ccf) {this.ccf = ccf;	}
	public String getHNE() {return hne;	}
	public void setHNE(String hne) {this.hne = hne;	}

	private String name, status;


	public String getName() {return name;	}
	public void setName(String name) {this.name = name;	}
	public String getStatus() {return status;	}
	public void setStatus(String status) {this.status = status;	}

}
