package net.skanny.myuml;

public class ClassRecctr {
	private String crc, rvv, bsk, trk, rq1, rq2, sqc;

	public ClassRecctr(){}

	public String getCRC() {return crc;	}
	public void setCRC(String crc) {this.crc = crc;	}
	public String getRVV() {return rvv;	}
	public void setRVV(String rvv) {this.rvv = rvv;	}
	public String getBSK() {return bsk;	}
	public void setBSK(String bsk) {this.bsk = bsk;	}
	public String getTRK() {return trk;	}
	public void setTRK(String trk) {this.trk = trk;	}
	public String getRQ1() {return rq1;	}
	public void setRQ1(String rq1) {this.rq1 = rq1;	}
	public String getRQ2() {return rq2;	}
	public void setRQ2(String rq2) {this.rq2 = rq2;	}
	public String getSQC() {return sqc;	}
	public void setSQC(String sqc) {this.sqc = sqc;	}


	private String name, status;


	public String getName() {return name;	}
	public void setName(String name) {this.name = name;	}
	public String getStatus() {return status;	}
	public void setStatus(String status) {this.status = status;	}
}
